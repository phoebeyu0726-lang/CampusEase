function buildRouteGraph() {
  const graph = Object.fromEntries(Object.keys(window.ROUTE_NODES).map((id) => [id, []]));

  ROUTE_EDGE_DATA.forEach(([from, to, config]) => {
    const fromNode = window.ROUTE_NODES[from];
    const toNode = window.ROUTE_NODES[to];
    const distance = config.distance || Math.round(Math.hypot(fromNode.x - toNode.x, fromNode.y - toNode.y) * 11);
    const makeEdge = (target, via) => ({
      to: target,
      distance,
      via: via || [],
      kind: config.kind || "outdoor",
      indoor: Boolean(config.indoor),
      crossing: Boolean(config.crossing),
      throughBuilding: config.throughBuilding || "",
      note: config.note || "",
      rainCover: config.rainCover ?? 40,
      shade: config.shade ?? 40,
      modeCost: config.modeCost || {},
      accessible: config.accessible !== false,
      hasStairs: config.accessible === false,
      slope: config.slope ?? (config.accessible === false ? 3 : 1)
    });

    graph[from].push(makeEdge(to, config.via || []));
    graph[to].push(makeEdge(from, [...(config.via || [])].reverse()));
  });

  return graph;
}

window.CAMPUS_GRAPH = buildRouteGraph();
